package ru.selfin.backend.model.enums;

/** Приоритет финансового события или категории. */
public enum Priority {
    /** Обязательный: должен быть исполнен. */
    HIGH,
    /** Необязательный: выполняется по возможности. Дефолт. */
    MEDIUM,
    /** Хотелка: откладывается в список нереализованных, если не исполнен. */
    LOW
}
